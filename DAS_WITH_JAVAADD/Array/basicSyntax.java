package DAS_WITH_JAVAADD.Array;

public class basicSyntax {
    public static void main(String[] args) {
        int[] arr=new int[5];
        arr[0]=1000;
        System.out.print(arr[0]+" ");
        //update
        arr[0]=200;
        System.out.print(arr[0]+" ");
        //addition
        arr[0]+=200;
        System.out.print(arr[0]+" ");

    }
}
